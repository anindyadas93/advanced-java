public class MultiDiamentionalArray{  
	public static void main(String args[]){  
		//declaring and initializing 2D array  
		int arr[][]={{1,2},{2,4}};  
		//printing 2D array  
		for(int i=0;i<2;i++){  
			for(int j=0;j<2;j++){  
				System.out.print(arr[i][j]+" ");  
			}  
			System.out.println();
		}
	}
}  